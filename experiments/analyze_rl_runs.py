    if duration > 0:
        run_record["trainer_model_time_share"] = (
            run_record["trainer_model_time_seconds"] / duration
        )
        run_record["trainer_collector_time_share"] = (
            run_record["trainer_collector_time_seconds"] / duration
        )
        run_record["trainer_test_time_share"] = run_record["trainer_test_time_seconds"] / duration
