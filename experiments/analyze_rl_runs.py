    n_rows = max(len(schedules), len(outcomes), len(rewards), len(lengths))

    for idx in range(n_rows):
        schedule = schedules[idx] if idx < len(schedules) else {}
        outcome = outcomes[idx] if idx < len(outcomes) else {}
        outcome_schedule = outcome.get("schedule", {}) if isinstance(outcome, dict) else {}
        schedule_payload = schedule if schedule else outcome_schedule
        reward = _safe_float(rewards[idx] if idx < len(rewards) else 0.0)
        length_steps = _safe_float(lengths[idx] if idx < len(lengths) else 0.0)
